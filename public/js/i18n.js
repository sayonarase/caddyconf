// ============================================================================
// i18n Runtime
// ============================================================================

window.currentLang = localStorage.getItem('caddyconfer-lang') || 'en';

function t(key) {
    const lang = window.currentLang;
    return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || (TRANSLATIONS.sv && TRANSLATIONS.sv[key]) || key;
}

function setLanguage(lang) {
    window.currentLang = lang;
    localStorage.setItem('caddyconfer-lang', lang);

    // Update data-i18n elements (textContent)
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (val !== key) el.textContent = val;
    });

    // Update data-i18n-html elements (innerHTML)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        const val = t(key);
        if (val !== key) el.innerHTML = val;
    });

    // Update data-i18n-title elements
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        const val = t(key);
        if (val !== key) el.title = val;
    });

    // Update data-i18n-placeholder elements
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const val = t(key);
        if (val !== key) el.placeholder = val;
    });

    // Update language toggle button
    const btn = document.getElementById('langToggle');
    if (btn) btn.innerHTML = lang === 'sv' ? '🇸🇪 SV' : '🇬🇧 EN';

    // Dispatch event for dynamic content
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

window.t = t;
window.setLanguage = setLanguage;
window.TRANSLATIONS = TRANSLATIONS;

// Auto-init
document.addEventListener('DOMContentLoaded', () => {
    if (window.currentLang !== 'sv') {
        setLanguage(window.currentLang);
    }
});
